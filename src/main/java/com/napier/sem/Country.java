package com.napier.sem;

/**
 * Country class
 *
 * Class containing all the variables
 * for storing information from the
 * country table from the World SQL
 * database
 */
public class Country
{
    /**
     * Code of the country
     */
    public String code;

    /**
     * Name of the country
     */
    public String name;

    /**
     * Continent the country belongs to
     */
    public String continent;

    /**
     * Region the country belongs to
     */
    public String region;

    /**
     * Population of the country
     */
    public int population;

    /**
     * Capital city code
     */
    public int capital_code;

    /**
     * Capital city name
     */
    public String capital_name;
}
